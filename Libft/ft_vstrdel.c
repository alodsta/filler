#include "libft.h"

void	ft_vstrdel(t_string **v)
{
	if (*v && (*v)->s)
	{
		free((*v)->s);
		(*v)->s = 0;
	}
	else if (*v)
	{
		free(*v);
		*v = 0;
	}
}
